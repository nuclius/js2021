let count = 0;

// Arithmetic Operators
count = count + 1;
count = count - 1;
count = count * 1;
count = count / 1;
count = count % 2;
count = count++;
count = ++count;
count = count--;
count = --count;
count = -count;
count = count ** 2;

// Comparison Operators
count == 0; // true/false
count != 0;
count === 0;
count !== 0;
count > 0;
count < 0;
count >= 0;
count <= 0;

// Logical Operators
count > 0 && count < 3;
count > 0 || count < 3;

// Assignment Operators
count = 0;
count += 1;
count -= 1;
count *= 2;
count /= 2;
count %= 1;
count **= 2;